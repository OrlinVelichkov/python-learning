# Step Slicing

# Task 1
nums = [10, 20, 30, 40, 50, 60]
print(nums[::2])
# Task 2
nums = [10, 20, 30, 40, 50, 60]
print(nums[1::2])
# Task 3
letters = ["A", "B", "C", "D", "E", "F"]
print(letters[::2])
# Task 4
letters = ["A", "B", "C", "D", "E", "F"]
print(letters[::-1])
# Task 5
names = ["Ivan", "Maria", "Petar", "Georgi", "Stefan", "Nikolay"]
print(names[::2])

